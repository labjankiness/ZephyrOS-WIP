#!/usr/bin/env sh
#
# build-localrepo.sh - Build selected AUR packages into a local pacman repo
#
# This script is intended to run on the build host (not inside the ISO).
# It produces:
#   build/localrepo/zephyros-local.db.tar.gz
#   build/localrepo/<pkg>.pkg.tar.zst
# and writes:
#   build/archiso/airootfs/etc/pacman.d/local.conf
#
# Packages built (AUR):
#   - adw-gtk3
#   - fluent-icon-theme-git
#   - archlinux-shim
#
# Requirements on the build host:
#   - base-devel (makepkg, fakeroot, etc.)
#   - git
#   - pacman-contrib (repo-add)
#
set -eu

SCRIPT_DIR=$(CDPATH= cd -- "$(dirname -- "$0")" && pwd)
BUILD_DIR=$(CDPATH= cd -- "$SCRIPT_DIR/.." && pwd)
ROOT_DIR=$(CDPATH= cd -- "$BUILD_DIR/.." && pwd)

LOCALREPO_DIR="$BUILD_DIR/localrepo"
SRC_DIR="$LOCALREPO_DIR/src"
PKGDEST_DIR="$LOCALREPO_DIR"
DB_NAME="zephyros-local"

LOCALCONF_PATH="$BUILD_DIR/archiso/airootfs/etc/pacman.d/local.conf"

FORCE=0

log() {
    printf '%s\n' "$*" >&2
}

abort() {
    log "ERROR: $*"
    exit 1
}

require_cmd() {
    command -v "$1" >/dev/null 2>&1 || abort "Required command '$1' not found."
}

ensure_dir() {
    mkdir -p "$1"
}

aur_clone_or_update() {
    PKG="$1"
    DEST="$SRC_DIR/$PKG"

    if [ -d "$DEST/.git" ]; then
        log "Updating AUR PKGBUILD: $PKG"
        (
            cd "$DEST" &&
            git fetch --all -p &&
            DEFAULT_BRANCH=$(git remote show origin | awk '/HEAD branch/ {print $NF}') &&
            git reset --hard "origin/$DEFAULT_BRANCH"
        )
    else
        log "Cloning AUR PKGBUILD: $PKG"
        git clone "https://aur.archlinux.org/$PKG.git" "$DEST"
    fi
}

build_aur_pkg() {
    PKG="$1"
    DEST="$SRC_DIR/$PKG"

    if [ "$FORCE" -ne 1 ]; then
        set -- "$PKGDEST_DIR/${PKG}-"*.pkg.tar.zst
        if [ -e "$1" ]; then
            log "Skipping $PKG (already built)"
            return 0
        fi
    fi

    log "Building: $PKG"
    (
        cd "$DEST"
        log "WARNING: Building $PKG with --skippgpcheck (dev builds only)."
        PKGDEST="$PKGDEST_DIR" makepkg -s --noconfirm --skippgpcheck
    )
}

write_localconf() {
    # pacman.conf used by mkarchiso runs on the build host, so this must point
    # to a build-host path.
    ABS_LOCALREPO=$(cd "$LOCALREPO_DIR" && pwd)
    ensure_dir "$(dirname "$LOCALCONF_PATH")"
    cat >"$LOCALCONF_PATH" <<EOF
# ZephyrOS local repo (generated by build/scripts/build-localrepo.sh)
#
# Built AUR packages are placed into:
#   $ABS_LOCALREPO
#
# Note: this repo uses TrustAll because it is local-only and generated as part
# of the build.

[${DB_NAME}]
SigLevel = Optional TrustAll
Server = file://$ABS_LOCALREPO
EOF
}

build_repo_db() {
    require_cmd repo-add

    DB_PATH="$LOCALREPO_DIR/${DB_NAME}.db.tar.gz"
    # shellcheck disable=SC2086
    PKGS=$(ls -1 "$LOCALREPO_DIR"/*.pkg.tar* 2>/dev/null || true)
    if [ -z "${PKGS:-}" ]; then
        abort "No built packages found in $LOCALREPO_DIR. Repo DB not created."
    fi

    log "Creating repo database: $DB_PATH"
    # shellcheck disable=SC2086
    repo-add -n -R "$DB_PATH" $PKGS >/dev/null
}

main() {
    if [ "$#" -gt 0 ]; then
        for arg in "$@"; do
            case "$arg" in
                --force) FORCE=1 ;;
                *)
                    abort "Unknown argument: $arg (supported: --force)"
                    ;;
            esac
        done
    fi

    require_cmd git
    require_cmd makepkg

    ensure_dir "$LOCALREPO_DIR"
    ensure_dir "$SRC_DIR"

    # AUR packages we bake into the ISO via a local repo.
    PKGS="adw-gtk3 fluent-icon-theme-git archlinux-shim"

    for p in $PKGS; do
        aur_clone_or_update "$p"
    done
    for p in $PKGS; do
        build_aur_pkg "$p"
    done

    build_repo_db
    write_localconf

    log "Local repo ready: $LOCALREPO_DIR"
    log "Pacman include written: $LOCALCONF_PATH"
}

main "$@"

